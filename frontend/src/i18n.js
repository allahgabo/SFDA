// Helper: translate country name based on language
export const translateCountry = (name, lang) => {
  if (!name) return name;
  if (lang === 'en') {
    return COUNTRY_MAP[name] || name;
  }
  // For Arabic, reverse lookup
  const reverseMap = Object.fromEntries(
    Object.entries(COUNTRY_MAP).map(([ar, en]) => [en, ar])
  );
  return reverseMap[name] || name;
};
